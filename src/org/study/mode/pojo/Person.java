package org.study.mode.pojo;

import java.sql.Date;

public class Person {
	private String userId;
	private String userName;
	private String userPass;
	private int age;
	private String sex;
	private Date dayOfBirth;
	private Date registrationdate;
	private String email;
	private String phone;
	private int limits;
	private String address;
	private int num;//分页，保存rownum
	public Person(String userId, String userName, String userPass, int age, String sex, Date dayOfBirth,
			Date registrationdate, String email, String phone, int limits, String address) {
		super();
		this.userId = userId;
		this.userName = userName;
		this.userPass = userPass;
		this.age = age;
		this.sex = sex;
		this.dayOfBirth = dayOfBirth;
		this.registrationdate = registrationdate;
		this.email = email;
		this.phone = phone;
		this.limits = limits;
		this.address = address;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getUserPass() {
		return userPass;
	}
	public void setUserPass(String userPass) {
		this.userPass = userPass;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public Date getDayOfBirth() {
		return dayOfBirth;
	}
	public void setDayOfBirth(Date dayOfBirth) {
		this.dayOfBirth = dayOfBirth;
	}
	public Date getRegistrationdate() {
		return registrationdate;
	}
	public void setRegistrationdate(Date registrationdate) {
		this.registrationdate = registrationdate;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public int getLimits() {
		return limits;
	}
	public void setLimits(int limits) {
		this.limits = limits;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String adress) {
		this.address = adress;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	@Override
	public String toString() {
		return "Person [userId=" + userId + ", userName=" + userName + ", userPass=" + userPass + ", age=" + age
				+ ", sex=" + sex + ", dayOfBirth=" + dayOfBirth + ", registrationdate=" + registrationdate + ", email="
				+ email + ", phone=" + phone + ", limits=" + limits + ", address=" + address + ", num=" + num + "]";
	}
}
